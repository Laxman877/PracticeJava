public class Identifiers {
    public static void main(String[] args) {
        int x = 121;
        int y = 60;
        int sum = x + y;
        System.out.println(sum);
        int sub = x - y;
        System.out.println(sub);
        int mul = x * y;
        System.out.println(mul);
        int div = x / y;
        System.out.println(div);
        int mdiv = x % y;
        System.out.println(mdiv);
    }
    
}
